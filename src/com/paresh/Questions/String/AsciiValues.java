package com.paresh.String;

public class AsciiValues {
    public static void main(String[] args) {
        System.out.println("ASCII Values from 0 to 255:");
        
        for (int i = 0; i <= 255; i++) {
            System.out.println("ASCII Value of --> " + (char) i + " <-- = " + i);
        }
    }
}

